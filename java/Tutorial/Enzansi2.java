class Enzansi2 {
    public static void main(String[] args){
        int x=10;
        int y=2;
        System.out.println(x>y);
        System.out.println(x<=y);
        System.out.println(x==y);//falseが出力される
        System.out.println(x!=y);//等しくない
        System.out.println(x>=5&&y>=5);
        System.out.println(x>=5||y>=5);//または
    }   
}